const rows = input.split(/\n/);

const treeCounter = (incrementRight, incrementDown) => {
  let counter = 0;
  for (
    let i = 0, j = 0;
    i < rows.length;
    i += incrementDown, j += incrementRight
  ) {
    let currentRow = rows[i];
    if (j > currentRow.length - 1) j -= currentRow.length;
    let currentElement = currentRow[j];
    if (currentElement === "#") counter++;
  }
  return counter;
};

